@Test("Test Xoshiro against reference implementation")
func xoshiroAgainstReference() {
    for n in UInt64(1) ... 50 {
        var xoshiro = Xoshiro256(seed: n)
        #expect(reference[Int(n)] == xoshiro.next())
    }
}

